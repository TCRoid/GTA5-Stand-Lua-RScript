local function CREATE_COLLECTIBLE_COMMANDS(menu_parent, menu_name, coords_table, menu_tables, has_collected)
    menu.action(menu_parent, "刷新收集状态", {}, "", function()
        for index, command in pairs(menu_tables) do
            local name = menu_name .. " " .. index
            if has_collected(index - 1) then
                name = name .. " [已收集]"
            end
            menu.set_menu_name(command, name)
        end
    end)

    for index, coords in pairs(coords_table) do
        local name = menu_name .. " " .. index
        if has_collected(index - 1) then
            name = name .. " [已收集]"
        end

        local menu_action = menu.action(menu_parent, name, {}, "", function()
            TP(coords)
        end)

        menu_tables[index] = menu_action
    end
end

